
function ds = Mecanico(t, s, z)

    ds = zeros(4,1);

    m1 = 290; b1 = 1000; m2 = 59; k1 = 16182;
    f = 0; k2 = 19000; zt = z(t);

    ds(1) = s(2);

    ds(2) = - b1*s(2)/m1 ...
            + b1*s(4)/m1 ...
            - k1*s(1)/m1 ...
            + k1*s(3)/m1 ...
            + f/m1;

    ds(3) = s(4);

    ds(4) =   b1*s(2)/m2 ...
            - b1*s(4)/m2 ...
            + k1*s(1)/m2 ...
            - k1*s(3)/m2 ...
            - k2*s(3)/m2 ...
            - f/m2 ...
            + k2*zt/m2;

end    